export interface LiveChatMessage {
  id: string;
  author: string;
  authorChannelId: string;
  text: string;
  timestampUsec: string;
  isPaid: boolean;
  paidAmount?: string;
}

export interface LiveChatResult {
  messages: LiveChatMessage[];
  continuation?: string;
  isLive: boolean;
  pollIntervalMs: number;
  videoTitle?: string;
  channelName?: string;
}

/**
 * Extracts live chat continuation token from video page
 */
async function getLiveChatContinuation(videoId: string): Promise<{
  continuation: string;
  isLive: boolean;
  visitorData: string;
  title?: string;
  author?: string;
}> {
  let html: string;

  try {
    html = await httpsRequest({
      hostname: 'www.youtube.com',
      path: `/watch?v=${videoId}`,
      method: 'GET',
      headers: {
        'User-Agent': WEB_USER_AGENT,
        'Accept-Language': 'en-US,en;q=0.9'
      }
    });
  } catch (err) {
    throw new Error(`Failed to fetch video page: ${(err as Error).message}`);
  }

  // Check if live
  const isLive = html.includes('"isLiveContent":true') || html.includes('"isLive":true');

  if (!isLive) {
    throw new Error('This video is not a live stream. Use get_transcript for regular videos.');
  }

  // Extract visitor data
  const visitorMatch = html.match(/"visitorData":"([^"]+)"/);
  const visitorData = visitorMatch?.[1] || '';

  // Extract continuation token specifically from live chat context
  // Try liveChatContinuation first, then liveChatRenderer, then broad fallback
  const liveChatContMatch = html.match(/"liveChatContinuation".*?"continuation":"([^"]+)"/s);
  const liveChatRendererMatch = html.match(/"liveChatRenderer".*?"continuation":"([^"]+)"/s);
  const continuationMatch = liveChatContMatch || liveChatRendererMatch || html.match(/"continuation":"([^"]+)"/);
  if (!continuationMatch) {
    throw new Error('Could not find live chat continuation token. The stream may have ended.');
  }

  // Extract metadata from videoDetails context (scoped to avoid unrelated matches)
  const videoDetailsMatch = html.match(/"videoDetails"\s*:\s*\{[^}]*?"title":"([^"]+)"[^}]*?"author":"([^"]+)"/s);
  const title = videoDetailsMatch?.[1] || html.match(/"title":"([^"]+)"/)?.[1];
  const author = videoDetailsMatch?.[2] || html.match(/"author":"([^"]+)"/)?.[1];

  return {
    continuation: continuationMatch[1],
    isLive,
    visitorData,
    title,
    author
  };
}

/**
 * Fetches live chat messages from a live stream
 */
export async function getLiveChat(options: {
  videoID: string;
  continuation?: string;
  visitorData?: string;
}): Promise<LiveChatResult & { visitorData: string }> {
  const { videoID } = options;
  let { continuation } = options;
  let visitorData = options.visitorData || '';
  let videoTitle: string | undefined;
  let channelName: string | undefined;

  // If no continuation provided, get it from the page
  if (!continuation) {
    const pageData = await getLiveChatContinuation(videoID);
    continuation = pageData.continuation;
    visitorData = pageData.visitorData;
    videoTitle = pageData.title;
    channelName = pageData.author;
  }

  // Fetch live chat
  const payload = JSON.stringify({
    context: {
      client: {
        clientName: 'WEB',
        clientVersion: DEFAULT_CLIENT_VERSION,
        visitorData: visitorData
      }
    },
    continuation: continuation
  });

  let response: string;
  try {
    response = await httpsRequest({
      hostname: 'www.youtube.com',
      path: '/youtubei/v1/live_chat/get_live_chat?key=AIzaSyAO_FJ2SlqU8Q4STEHLGCilw_Y9_11qcW8',
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Content-Length': Buffer.byteLength(payload),
        'User-Agent': WEB_USER_AGENT,
        'Origin': 'https://www.youtube.com'
      }
    }, payload);
  } catch (err) {
    throw new Error(`Failed to fetch live chat: ${(err as Error).message}`);
  }

  let json: any;
  try {
    json = JSON.parse(response);
  } catch (parseErr) {
    console.error(`[getLiveChat] Failed to parse response as JSON: ${(parseErr as Error).message}`);
    throw new Error(`Live chat response is not valid JSON: ${(parseErr as Error).message}`);
  }

  if (json.error) {
    throw new Error(`YouTube API error: ${json.error.message || 'Unknown error'}`);
  }

  const messages: LiveChatMessage[] = [];
  let nextContinuation: string | undefined;
  let pollIntervalMs = 5000; // Default 5 seconds

  // Extract continuation and poll interval
  const liveChatContinuation = json.continuationContents?.liveChatContinuation;
  if (liveChatContinuation) {
    const continuations = liveChatContinuation.continuations || [];
    for (const cont of continuations) {
      const invalidation = cont.invalidationContinuationData || cont.timedContinuationData;
      if (invalidation) {
        nextContinuation = invalidation.continuation;
        pollIntervalMs = invalidation.timeoutMs || 5000;
        break;
      }
    }

    // Extract messages from actions
    const actions = liveChatContinuation.actions || [];
    for (const action of actions) {
      if (action.addChatItemAction) {
        const item = action.addChatItemAction.item;

        // Regular text message
        if (item.liveChatTextMessageRenderer) {
          const r = item.liveChatTextMessageRenderer;
          const text = (r.message?.runs || []).map((run: any) => run.text || '').join('');

          messages.push({
            id: r.id || '',
            author: r.authorName?.simpleText || '',
            authorChannelId: r.authorExternalChannelId || '',
            text,
            timestampUsec: r.timestampUsec || '',
            isPaid: false
          });
        }

        // Paid message (Super Chat)
        if (item.liveChatPaidMessageRenderer) {
          const r = item.liveChatPaidMessageRenderer;
          const text = (r.message?.runs || []).map((run: any) => run.text || '').join('');

          messages.push({
            id: r.id || '',
            author: r.authorName?.simpleText || '',
            authorChannelId: r.authorExternalChannelId || '',
            text,
            timestampUsec: r.timestampUsec || '',
            isPaid: true,
            paidAmount: r.purchaseAmountText?.simpleText || ''
          });
        }
      }
    }
  }

  return {
    messages,
    continuation: nextContinuation,
    isLive: Boolean(nextContinuation),
    pollIntervalMs,
    videoTitle,
    channelName,
    visitorData
  };
}

// ==================== BACKGROUND STREAMING ====================

interface LiveStreamState {
  videoId: string;
  visitorData: string;
  continuation: string;
  pollIntervalMs: number;
  messageBuffer: LiveChatMessage[];
  seenIds: Set<string>;
  isPolling: boolean;
  pollTimer: NodeJS.Timeout | null;
  videoTitle?: string;
  channelName?: string;
  startedAt: number;
  lastPollAt: number;
  totalMessagesFetched: number;
}

// Global state for background streams
const activeStreams: Map<string, LiveStreamState> = new Map();

/**
 * Check if a video is a live stream without throwing
 */
export async function checkIfLive(videoId: string): Promise<{
  isLive: boolean;
  title?: string;
  author?: string;
}> {
  try {
    const html = await httpsRequest({
      hostname: 'www.youtube.com',
      path: `/watch?v=${videoId}`,
      method: 'GET',
      headers: {
        'User-Agent': WEB_USER_AGENT,
        'Accept-Language': 'en-US,en;q=0.9'
      }
    });

    const isLive = html.includes('"isLiveContent":true') || html.includes('"isLive":true');
    // Scope to videoDetails to avoid matching recommended video titles
    const videoDetailsMatch = html.match(/"videoDetails"\s*:\s*\{[^}]*?"title":"([^"]+)"[^}]*?"author":"([^"]+)"/s);
    const title = videoDetailsMatch?.[1] || html.match(/"title":"([^"]+)"/)?.[1];
    const author = videoDetailsMatch?.[2] || html.match(/"author":"([^"]+)"/)?.[1];

    return {
      isLive,
      title,
      author
    };
  } catch {
    return { isLive: false };
  }
}

/**
 * Start background polling for a live stream
 */
export async function startLiveChatStream(videoId: string): Promise<LiveChatResult & { streaming: boolean }> {
  // Check if already streaming
  if (activeStreams.has(videoId)) {
    const state = activeStreams.get(videoId)!;
    // Return buffered messages and clear buffer
    const messages = [...state.messageBuffer];
    state.messageBuffer = [];

    return {
      messages,
      continuation: state.continuation,
      isLive: state.isPolling,
      pollIntervalMs: state.pollIntervalMs,
      videoTitle: state.videoTitle,
      channelName: state.channelName,
      streaming: true
    };
  }

  // Start new stream
  const result = await getLiveChat({ videoID: videoId });

  const state: LiveStreamState = {
    videoId,
    visitorData: result.visitorData,
    continuation: result.continuation || '',
    pollIntervalMs: result.pollIntervalMs,
    messageBuffer: [],
    seenIds: new Set(result.messages.map(m => m.id)),
    isPolling: true,
    pollTimer: null,
    videoTitle: result.videoTitle,
    channelName: result.channelName,
    startedAt: Date.now(),
    lastPollAt: Date.now(),
    totalMessagesFetched: result.messages.length
  };

  activeStreams.set(videoId, state);

  // Start background polling
  const poll = async () => {
    if (!state.isPolling) return;

    try {
      const newResult = await getLiveChat({
        videoID: videoId,
        continuation: state.continuation,
        visitorData: state.visitorData
      });

      state.lastPollAt = Date.now();
      state.continuation = newResult.continuation || state.continuation;
      state.pollIntervalMs = newResult.pollIntervalMs;

      // Count new messages before adding to seenIds
      let newMsgCount = 0;

      // Add only new messages to buffer
      for (const msg of newResult.messages) {
        if (!state.seenIds.has(msg.id)) {
          state.seenIds.add(msg.id);
          state.messageBuffer.push(msg);
          state.totalMessagesFetched++;
          newMsgCount++;
        }
      }

      // Cap buffer at 500 messages
      if (state.messageBuffer.length > 500) {
        state.messageBuffer = state.messageBuffer.slice(-500);
      }

      // Cap seenIds at 2000
      if (state.seenIds.size > 2000) {
        const arr = Array.from(state.seenIds);
        state.seenIds = new Set(arr.slice(-1500));
      }

      // Log new messages to stderr
      if (newMsgCount > 0) {
        process.stderr.write(`\n[LIVE] ${state.videoTitle || videoId} | +${newMsgCount} msgs | buffer: ${state.messageBuffer.length} | total: ${state.totalMessagesFetched}\n`);
        const lastMsg = state.messageBuffer[state.messageBuffer.length - 1];
        if (lastMsg) {
          process.stderr.write(`  └─ ${lastMsg.author}: ${lastMsg.text.slice(0, 60)}${lastMsg.text.length > 60 ? '...' : ''}\n`);
        }
      }

    } catch (err) {
      console.error(`[live-chat] Poll error for ${videoId}:`, err);
      // Stream may have ended
      if ((err as Error).message?.includes('ended')) {
        state.isPolling = false;
        if (state.pollTimer) clearTimeout(state.pollTimer);
        activeStreams.delete(videoId);
        return;
      }
    }

    // Schedule next poll
    if (state.isPolling) {
      state.pollTimer = setTimeout(poll, state.pollIntervalMs);
    }
  };

  // Start first background poll after initial delay
  state.pollTimer = setTimeout(poll, state.pollIntervalMs);

  return {
    ...result,
    streaming: true
  };
}

/**
 * Get buffered messages from an active stream
 */
export function getLiveChatBuffer(videoId: string): {
  messages: LiveChatMessage[];
  isStreaming: boolean;
  stats: {
    totalFetched: number;
    bufferSize: number;
    streamDurationMs: number;
    lastPollMs: number;
  };
  videoTitle?: string;
  channelName?: string;
} | null {
  const state = activeStreams.get(videoId);
  if (!state) return null;

  // Get and clear buffer
  const messages = [...state.messageBuffer];
  state.messageBuffer = [];

  return {
    messages,
    isStreaming: state.isPolling,
    stats: {
      totalFetched: state.totalMessagesFetched,
      bufferSize: messages.length,
      streamDurationMs: Date.now() - state.startedAt,
      lastPollMs: Date.now() - state.lastPollAt
    },
    videoTitle: state.videoTitle,
    channelName: state.channelName
  };
}

/**
 * Stop background polling for a live stream
 */
export function stopLiveChatStream(videoId: string): {
  stopped: boolean;
  finalMessages: LiveChatMessage[];
  stats: {
    totalFetched: number;
    streamDurationMs: number;
  };
} | null {
  const state = activeStreams.get(videoId);
  if (!state) return null;

  state.isPolling = false;
  if (state.pollTimer) {
    clearTimeout(state.pollTimer);
  }

  const finalMessages = [...state.messageBuffer];
  const stats = {
    totalFetched: state.totalMessagesFetched,
    streamDurationMs: Date.now() - state.startedAt
  };

  activeStreams.delete(videoId);

  return {
    stopped: true,
    finalMessages,
    stats
  };
}

/**
 * Stop all active streams and release resources. Call on process exit.
 */
export function stopAllStreams(): void {
  for (const [, state] of activeStreams) {
    state.isPolling = false;
    if (state.pollTimer) clearTimeout(state.pollTimer);
  }
  activeStreams.clear();
}

