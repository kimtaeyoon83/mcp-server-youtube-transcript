import https from 'https';

/**
 * Error thrown when transcript fetch fails but page data was retrieved.
 * Carries isLive and metadata so callers can redirect to live chat.
 */
export class TranscriptFetchError extends Error {
  isLive: boolean;
  metadata: VideoMetadata;
  constructor(message: string, isLive: boolean, metadata: VideoMetadata) {
    super(message);
    this.name = 'TranscriptFetchError';
    this.isLive = isLive;
    this.metadata = metadata;
  }
}

